package day3;

public class ForloopExample {

	public static void main(String[] args) {
		
		// num += 2  ~ num = num + 2
		
		for(int num = 0; num < 10 ; num += 2 ) {
		
			System.out.println("number - " + num );
		
		}
		
		for(int num = 10; num > 0 ; num-- ) {
			
			System.out.println("number - " + num );
		
		}

	}

}
 